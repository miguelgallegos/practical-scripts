<?php

function solution($A) {
    $pairs = 0;
    $east = 0;
    for ($i = 0; $i < count($A); $i ++) {
        // east
        if (0 == $A[$i]) {
            $east ++;
        } else {
            $pairs += $east;            
        }
    }

    return ($pairs > 1000000000) ? -1 : $pairs;
}

$A = [0, 1, 0, 1, 1];
